public class UserImpact {

    private int storiesSharedCount;

    private int graphicEarnedCount;

    private int starsReviewCount;

    public int getStoriesSharedCount() {
        return storiesSharedCount;
    }

    public void setStoriesSharedCount(int storiesSharedCount) {
        this.storiesSharedCount = storiesSharedCount;
    }

    public int getGraphicEarnedCount() {
        return graphicEarnedCount;
    }

    public void setGraphicEarnedCount(int graphicEarnedCount) {
        this.graphicEarnedCount = graphicEarnedCount;
    }

    public int getStarsReviewCount() {
        return starsReviewCount;
    }

    public void setStarsReviewCount(int starsReviewCount) {
        this.starsReviewCount = starsReviewCount;
    }
}
